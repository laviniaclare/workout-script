FLAG_TO_MOVES_MAP = {
	'-bw': BODY_WEIGHT,
	'-bl': BLOCKS,
	'-mb': MINI_BAND,
	'-rb': RESISTENCE_BAND,
	'-h': HOLDS,
	'-o': OTHER_TOYS,
	'-kb': KETTLEBELL,
}

def generate_workout(num_exercises):
	"""
	"""
	exercises = []
	for flag in sys.argv[1:]:
		exercises += FLAG_TO_MOVES_MAP[flag]

	print('Your assignment is:\n')
	print(' - 5 Burpees')
	for _ in range(num_exercises):
		move_index = random.randint(0, len(exercises)-1)
		move = exercises[move_index]
		print(' - '+move)

	print('\nGood luck!')

if __name__ == '__main__':
	num_exercises = input('How many different moves do you want to do?\n')
	generate_workout(num_exercises)
